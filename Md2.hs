putFrame :: Frame -> Put
putFrame Frame{frameScale = (sx,sy,sz), frameTranslate = (tx,ty,tz), ..} = do
  putFloat32le sx
  putFloat32le sy
  putFloat32le sz
  putFloat32le tx
  putFloat32le ty
  putFloat32le tz
  put frameName
  put frameTriVerts


getFrame :: Int -> Get Frame
getFrame numVert = do
  scale <- (,,) <$> getFloat32le <*> getFloat32le <*> getFloat32le
  translate <- (,,) <$> getFloat32le <*> getFloat32le <*> getFloat32le
  name <- getByteString 16
  verts <- sequence (replicate numVert get)
  return $ Frame
    { frameScale = scale
    , frameTranslate = translate
    , frameName = name
    , frameTriVerts = verts
    }


instance Binary Tri where
  put (Tri (vx,vy,vz) (tx,ty,tz)) = do
    putWord16le vx
    putWord16le vy
    putWord16le vz
    putWord16le tx
    putWord16le ty
    putWord16le tz
  get = Tri <$> g <*> g
   where g = (,,) <$> getWord16le <*> getWord16le <*> getWord16le


instance Binary GLCmd where
  put GLCmd{..} = do
    putFloat32le glCmdS
    putFloat32le glCmdT
    putWord32le glCmdVertIdx
  get = GLCmd <$> getFloat32le <*> getFloat32le <*> getWord32le


instance Binary Md2 where
  put Md2{..} = do
    return ()
  get = do
    header@Md2Header{..} <- get
    buffer <- getLazyByteString (fromIntegral md2HeaderOfsEnd - 68)

    let getBuf n o s = BL.take (fromIntegral n * fromIntegral s)
                     . BL.drop (fromIntegral o)
                     $ buffer

    let readBuf n g o s = runGet (sequence $ replicate (fromIntegral n) g)
                                 (getBuf n (o - 68) s)

    let skins = readBuf md2HeaderNumSkins (getByteString 64)
                        md2HeaderOfsSkins 64
    let tris = readBuf md2HeaderNumTris get
                       md2HeaderOfsTris (size16 * 6)
    let texCoords = readBuf md2HeaderNumSt ((,) <$> getWord16le <*> getWord16le)
                            md2HeaderOfsSt (size16 * 2)
    let frames = readBuf md2HeaderNumFrames (getFrame $ fromIntegral md2HeaderNumXyz)
                         md2HeaderOfsFrames md2HeaderFramesize
    let glCmds = readBuf (md2HeaderNumGlcmds `div` 3) get
                         md2HeaderOfsGlcmds (size32 * 3)

    return $ Md2
      { md2Header = header
      , md2Skins = skins
      , md2Frames = frames
      , md2Tris = tris
      , md2St = texCoords
      , md2GLCmd = glCmds
      }
   where
    size16 = sizeOf (undefined :: Word16)
    size32 = sizeOf (undefined :: Word32)


